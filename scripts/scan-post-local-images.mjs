#!/usr/bin/env node
/**
 * Scans public/mock/posts for files named {강남|광화문}_{NNN}.{ext}
 * Writes src/data/post-local-images-manifest.gen.ts (committed; re-run when assets change).
 */
import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const DIR = path.join(ROOT, "public/mock/posts");
const OUT = path.join(ROOT, "src/data/post-local-images-manifest.gen.ts");

const REGION_RE = /^(강남|광화문)_(\d+)\.[^.]+$/u;

function main() {
  if (!fs.existsSync(DIR)) {
    fs.mkdirSync(DIR, { recursive: true });
  }
  const files = fs.existsSync(DIR) ? fs.readdirSync(DIR) : [];
  /** @type {Map<string, { region: string, num: number, file: string }>} */
  const byLogical = new Map();

  for (const file of files) {
    const stat = fs.statSync(path.join(DIR, file));
    if (!stat.isFile()) continue;
    const nfc = file.normalize("NFC");
    const m = nfc.match(REGION_RE);
    if (!m) continue;
    const region = m[1];
    const num = parseInt(m[2], 10);
    const key = `${region}_${String(num).padStart(3, "0")}`;
    if (!byLogical.has(key)) {
      byLogical.set(key, { region, num, file });
    }
  }

  const gangnam = [];
  const gwanghwamun = [];
  for (const { region, num, file } of byLogical.values()) {
    const url = `/mock/posts/${file}`;
    if (region === "강남") gangnam.push({ num, url });
    else gwanghwamun.push({ num, url });
  }
  gangnam.sort((a, b) => a.num - b.num);
  gwanghwamun.sort((a, b) => a.num - b.num);

  const body = `/* eslint-disable */
/* Auto-generated by scripts/scan-post-local-images.mjs — do not edit by hand. */

export const POST_LOCAL_IMAGE_POOL_GANGNAM: readonly string[] = ${JSON.stringify(gangnam.map((x) => x.url), null, 2)} as const;

export const POST_LOCAL_IMAGE_POOL_GWANGHWAMUN: readonly string[] = ${JSON.stringify(gwanghwamun.map((x) => x.url), null, 2)} as const;
`;

  fs.writeFileSync(OUT, body, "utf8");
  console.log(`Wrote ${OUT} (${gangnam.length} gangnam, ${gwanghwamun.length} gwanghwamun)`);
}

main();
